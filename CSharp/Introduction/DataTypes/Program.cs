
Console.WriteLine('A'); //character output
Console.WriteLine("Hello, World!"); //string output
Console.WriteLine("The current time is " + DateTime.Now); //string output
Console.WriteLine(123); //integer output
Console.WriteLine(123 + 456); //integer output 
Console.WriteLine(123.456F); //float output F/f is used to specify the float type
Console.WriteLine(123.456F + 789.123F); //float output
Console.WriteLine(123.456); //double output  
Console.WriteLine(123.456 + 789.123); //double output 
Console.WriteLine(12.39816m); //decimal output M/m is used to specify the decimal type
Console.WriteLine(123.456m + 789.123m); //decimal output
Console.WriteLine(true); //boolean output 
Console.WriteLine(false); //boolean output

// Console.WriteLine(null); //null output
Console.WriteLine("123"); //string output
Console.WriteLine(123); //integer output

Console.WriteLine("true"); //string output
Console.WriteLine(true); //boolean output

